package E99;


public class Persona {

    String nombre;
    String apellidos;
    int edad;
    boolean casado;
    
    public Persona(String nombre, String apellidos, int edad, boolean casado) {
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.edad = edad;
        this.casado = casado;
    }
    
    
}
