    private void pulsarBombilla(java.awt.event.ActionEvent evt) {
        JButton boton = (JButton) evt.getSource(); // pillo el boton que ha saltado el evento
        cambiarEstado(boton);
        reproducirSonido();
    }

    private void cambiarEstado(JButton boton) {
        int botonName = Integer.parseInt(boton.getName()); // paso la "id" a int
        if (boton.getName().equals("0")) { // si es el primero, que solo cambie el de la derecha
            bombillas.get(botonName + 1).setIcon((bombillas.get(botonName + 1).getIcon().toString().equals(encendido)) ? new ImageIcon(apagado) : new ImageIcon(encendido));
        } else if (boton.getName().equals(String.valueOf(bombillas.size() - 1))) { // si es el ultimo que cambie el de la izquierda
            bombillas.get(botonName - 1).setIcon((bombillas.get(botonName - 1).getIcon().toString().equals(encendido)) ? new ImageIcon(apagado) : new ImageIcon(encendido));
        } else { // si no es ultimo ni primero
            bombillas.get(botonName - 1).setIcon((bombillas.get(botonName - 1).getIcon().toString().equals(encendido)) ? new ImageIcon(apagado) : new ImageIcon(encendido));
            bombillas.get(botonName + 1).setIcon((bombillas.get(botonName + 1).getIcon().toString().equals(encendido)) ? new ImageIcon(apagado) : new ImageIcon(encendido));
            // en estas condiciones compruebo que si esta encendido se apaga y viceversa
        }
        intentos++;
        intentoLabel.setText("Intentos: " + String.valueOf(intentos));
        comprobarEstados();
    }

    private void comprobarEstados() {
        int contador = 0;
        for (JButton boton : bombillas) {
            if (boton.getIcon().toString().equals(encendido)) {
                contador++;
            }
        }
        if (contador == bombillas.size()) {
            JOptionPane.showMessageDialog(this, "Has ganado!");
            insertarImagenes();
            intentos = 0;
            intentoLabel.setText("Intentos: " + String.valueOf(intentos));
        }
    }

