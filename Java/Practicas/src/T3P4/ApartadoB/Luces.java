    private void elegirBotones() {
        do {
            String cantidadBotones = JOptionPane.showInputDialog("Elige la cantidad de botones (3 a 20)");
            try {
                numBotones = Integer.parseInt(cantidadBotones);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Debe ser un numero entero");
            }
        } while (numBotones < 3 || numBotones > 20);
    }

    private void eventoBotones() {
        bombillas = new ArrayList<>();
        for (int i = 0; i < numBotones; i++) {
            JButton boton = new JButton();
            bombillas.add(boton);
            jPanel1.add(boton);
        }
        for (JButton button : bombillas) {
            button.addActionListener(new java.awt.event.ActionListener() {
                @Override
                public void actionPerformed(java.awt.event.ActionEvent evt) {
                    pulsarBombilla(evt);
                }
            });
        }
    }

    private void asignarID() {
        for (int i = 0; i < bombillas.size(); i++) {
            bombillas.get(i).setName(String.valueOf(i));
        }
    }

    private void insertarImagenes() {
        for (JButton button : bombillas) {
            button.setIcon(new ImageIcon(URL_assets + "0.png"));
        }
    }

    private void reproducirSonido() {
        try {
            AudioInputStream audio = AudioSystem.getAudioInputStream(new File("src/moyano_elliot/ApartadoB/assets/sonido.wav").getAbsoluteFile());
            Clip clip = AudioSystem.getClip();
            clip.open(audio);
            clip.start();
        } catch (UnsupportedAudioFileException | IOException | LineUnavailableException e) {
            System.out.println("Error al reproducir sonido");
        }
    }

