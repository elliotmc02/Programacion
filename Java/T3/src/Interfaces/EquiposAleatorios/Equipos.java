    private void hacerEquipos(ArrayList<String> listaJugadores, int numJugadores) {
        ArrayList<String> parejas = new ArrayList<>();
        int fila = 0;
        int columna = 0;
        int nRandom;
        DefaultTableModel model = (DefaultTableModel) tabla.getModel();
        model.setRowCount(0);
        do {
            model.addRow(new Object[]{});
        } while (model.getRowCount() != (Math.ceil(Double.parseDouble(String.valueOf(numJugadores)) / 4)));
        while (parejas.size() < listaJugadores.size()) {
            String j1;
            do {
                nRandom = (int) (Math.random() * listaJugadores.size());
                j1 = listaJugadores.get(nRandom);
            } while (parejas.contains(listaJugadores.get(nRandom)));
            parejas.add(j1);
            String j2 = "";
            if (parejas.size() != listaJugadores.size()) {
                do {
                    nRandom = (int) (Math.random() * listaJugadores.size());
                    j2 = listaJugadores.get(nRandom);
                } while (parejas.contains(listaJugadores.get(nRandom)));
            }
            parejas.add(j2);

            if (listaJugadores.size() % 4 == 0) {
                model.setValueAt(j1 + (!j2.isBlank() ? " y " + j2 : ""), fila, columna);
            } else if (listaJugadores.size() == parejas.size()) {
                model.setValueAt(j1, fila, columna);
                model.setValueAt(j2, fila, columna + 1);
            } else {
                model.setValueAt(j1 + (!j2.isBlank() ? " y " + j2 : ""), fila, columna);
            }

            columna++;

            if (columna == 2) {
                columna = 0;
                fila++;
            }

        }
        tablaFrame.setVisible(true);
        setVisible(false);
    }

