    private void nJugadoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nJugadoresActionPerformed
        int MAX_JUGADORES = 16;
        int MIN_JUGADORES = 4;

        if (!nJugadores.getText().isBlank()) {
            try {
                int numJugadores = Integer.parseInt(nJugadores.getText());
                if (numJugadores < MIN_JUGADORES || numJugadores > MAX_JUGADORES) {
                    JOptionPane.showMessageDialog(this, (numJugadores > MAX_JUGADORES) ? "Ni Norberto tiene esa cantidad de contactos" : "Debe ser " + MIN_JUGADORES + " o m√°s subnormal", "Error", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                listaJugadores = new ArrayList<>();
                int contador = 0;
                while (contador < numJugadores) {
                    String input = JOptionPane.showInputDialog(this, "Jugador " + (contador + 1), "Jugadores", -1);
                    if (input == null) {
                        return;
                    }
                    if (input.isBlank() || listaJugadores.contains(input)) {
                        JOptionPane.showMessageDialog(this, input.isBlank() ? "No puedes dejarlo en blanco" : "No puedes repetir nombres", "Error", JOptionPane.WARNING_MESSAGE);
                    } else {
                        listaJugadores.add(input);
                        contador++;
                    }

                }
                hacerEquipos(listaJugadores, numJugadores);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Debe ser un numero entero", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Introduzca un valor", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_nJugadoresActionPerformed

